package org.francalderon.app.fotocabina.models;

public class FotoDTO {
    private double alto;
    private double X;
    private double y;

    public FotoDTO() {
    }

    public double getAlto() {
        return alto;
    }

    public void setAlto(double alto) {
        this.alto = alto;
    }


    public double getX() {
        return X;
    }

    public void setX(double x) {
        this.X = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
